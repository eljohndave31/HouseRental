  public static boolean loginAcc(String username, String password){
       try{ ResultSet rs = new dbConnector().getData("SELECT * FROM rh WHERE user = '"+username+"' AND pass = '"+password+"'");
       return rs.next();
       }catch(SQLException er){return false;}
    }
    public static void showTable(DefaultTableModel tb){
        try{
        ResultSet rs = new dbConnector().getData("SELECT * FROM rh");
        while(rs.next()){
                tb.addRow(new String[] {rs.getString("id"),rs.getString("user"),rs.getString("pass")});
        }
        }catch(SQLException er){}
    }


